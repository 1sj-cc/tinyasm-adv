package nebula.tinyasm;

public class MethodCodeFriendlyLogicSample {
	public int and(int x, int y) {
		return x & y;
	}

	public int or(int x, int y) {
		return x | y;
	}

	public int xor(int x, int y) {
		return x ^ y;
	}

	public byte and(byte x, byte y) {
		return (byte) (x & y);
	}

	public byte or(byte x, byte y) {
		return (byte) (x | y);
	}

	public byte xor(byte x, byte y) {
		return (byte) (x ^ y);
	}

	public short and(short x, short y) {
		return (short) (x & y);
	}

	public short or(short x, short y) {
		return (short) (x | y);
	}

	public short xor(short x, short y) {
		return (short) (x ^ y);
	}

	public long and(long x, long y) {
		return (long) (x & y);
	}

	public long or(long x, long y) {
		return (long) (x | y);
	}

	public long xor(long x, long y) {
		return (long) (x ^ y);
	}

	public char and(char x, char y) {
		return (char) (x & y);
	}

	public char or(char x, char y) {
		return (char) (x | y);
	}

	public char xor(char x, char y) {
		return (char) (x ^ y);
	}
}
